			<p class="lead" align="justify">Una vez que todos los líderes participantes del conglomerado minorista completaron el programa Genos, la transformación fue evidente para todos. Los líderes eran ahora mucho más conscientes de sí mismos y estaban totalmente enterados del efecto que podían tener sus propias emociones y estados de ánimo sobre sus reportes directos. Los líderes eran ahora más competentes al expresarse con sinceridad, provocando que la gente respondiera del mismo modo. La impresión que proyectaban los líderes cambió dramáticamente y los demás los percibían ahora como positivos y dignos de confianza. Los líderes también habían aprendido a utilizar la información emocional para practicar el razonamiento emocional de manera proactiva.<br><br>

