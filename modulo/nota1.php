			<h3>GENOS INTERNATIONAL IMPULSA EL CAMBIO A TRAVÉS DE LA INTELIGENCIA EMOCIONAL</h3>
			<img src="http://via.placeholder.com/1110x280" class="img-responsive" style="width: auto;">
			<br><h5>Resumen</h5>
			<p class="lead text-justify">
			Este estudio de caso refiere nuestro trabajo con un importante conglomerado minorista de Australia que abarca una cantidad considerable de subsidiarias y marcas. Este gigante minorista estaba explorando la posibilidad de migrar a un modelo de servicios compartidos y eligió a Genos para que le ayude en esta transición.

			Los proyectos de cambio a gran escala, que necesitan implementarse a fin de facilitar una operación centralizada para diferentes departamentos de la organización, exigían que áreas transversales de soporte, tales como Recursos Humanos, Finanzas y TI, experimenten perturbaciones importantes y, por consiguiente, se observó cierto deterioro en las operaciones. Como es muy común con anuncios importantes de este tipo, estos cambios a gran escala pueden tener un efecto dramático en el compromiso, desempeño y conductas negativas de los trabajadores, así como generar dificultades en la retención de talento. Esta reacción posiblemente podía perjudicar el modelo de servicios compartidos proyectado
			</p>	
			<h5>La Solución</h5>
			<p class="lead text-justify">
			

				Genos hizo una profunda investigación y formuló finalmente un programa de desarrollo de liderazgo estratégico que buscaba incrementar y mejorar la capacidad de los líderes del equipo del proyecto de interactuar con los demás de manera positiva e influir en su resistencia al cambio. Este programa pretendía mejorar el compromiso de los trabajadores y e incrementar la moral general de la organización, a todos los niveles.<br>

				Usando el modelo de inteligencia emocional desarrollado por Genos, creamos un programa que incluía:
			</p>

				<ul style="padding-top: 40px;" class="lead text-justify">
					<li>Una evaluación 360º de inteligencia emocional para cada líder. Este informe nos permitió medir, desde su origen, los niveles de compromiso de los trabajadores.</li>
					<li>Talleres – Genos dictó un taller de un día completo y dos talleres de medio día con todos los líderes, para educarlos acerca de los beneficios de usar la inteligencia emocional en el trabajo. En estos talleres las personas aprendieron cómo influir positivamente en sus reportes directos y solucionar cualquier obstáculo al cambio.</li>
					<li>Genos organizó 7 clases individuales de coaching con los líderes. Estas sesiones se desarrollaron individualmente y sentaron las bases del éxito futuro. Las sesiones se enfocaron en educar a los líderes en el arte de la persuasión, entendimiento, expresión y creación de emociones positivas en el entorno de trabajo.</li>
					<li>Una evaluación 360º de los niveles de inteligencia emocional de todos los líderes y una evaluación de los trabajadores que reportaban a ellos. Este informe nos permitió calibrar el nivel de compromiso de los trabajadores y nos mostró con claridad el retorno de la inversión generada por el programa de Genos.</li>
				</ul>
				<p class="lead text-justify">Muchos años de investigación han demostrado, de manera indiscutible, que es evidente la relación entre un sólido resultado de negocios y el alto nivel de compromiso de los trabajadores. Las compañías que exhiben altos niveles de compromiso de sus trabajadores versus competidores con un bajo nivel de compromiso, muestran utilidades extraordinariamente más altas, en un nivel promedio de 3.9 veces por acción. Eso significa casi ¡4 veces más!</p>


