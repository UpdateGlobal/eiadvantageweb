				<h3>¿Qué es Compromiso de los Trabajadores?</h3>
				<h5>Desde la perspectiva del trabajador:</h5>
				<p class="lead">El apego emocional que sienten las personas por su centro de trabajo, su rol, posición dentro de la compañía, sus colegas y cultura y el efecto que este apego ejerce sobre el bienestar y la productividad.</p>
				<h5>Desde el punto de vista de la organización:</h5>
				<p class="lead">El compromiso de los trabajadores tiene que ver con el uso de las nuevas medidas e iniciativas para incrementar el apego emocional positivo que sienten y, por lo tanto, la productividad y el éxito general del negocio.</p>
				

