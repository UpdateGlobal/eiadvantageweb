			<h3>GENOS INTERNATIONAL AYUDA A INCREMENTAR LAS VENTAS DE SANOFI-AVENTIS</h3>
			<img src="http://via.placeholder.com/1110x280" class="img-responsive" style="width: auto;">
				<br>	
			<h5>Resumen</h5>
			<p class="lead text-justify">Sanofi-Aventis es una de las organizaciones farmacéuticas más prominentes del mundo y tiene globalmente más de 105,000 trabajadores. Con presencia directa en más de 110 países, Sanofi-Aventis es actualmente la cuarta compañía farmacéutica más grande del mundo.<br><br>

				Antes trabajar con Sanofi-Aventis, Genos había emprendido investigaciones en el dominio de las ventas farmacéuticas y correlacionado información que demostraba una correspondencia directa entre el desempeño de un representante de ventas y su inteligencia emocional. Sanofi-Aventis estaba totalmente de acuerdo con nuestras conclusiones, sin embargo, aún tenía algunas interrogantes propias. ¿Se podía mejorar la inteligencia emocional de un representante de ventas? De ser así, ¿generaría este mayor nivel de inteligencia emocional un incremento de los ingresos de ventas?
				</p>
			

			<h5>Nuestra Solución</h5>
			<p class="lead text-justify">A fin de asegurarnos de proporcionar a Sanofi-Aventis información correcta, necesitábamos eliminar cualquier influencia del mercado que pudiera afectar nuestros resultados. Genos solicitó a Sanofi-Aventis que seleccione un grupo de representantes de ventas en el que todos mostraran niveles similares de desempeño de ventas y que compartieran el mismo nivel de responsabilidad en la organización. Genos seleccionó 70 candidatos que participarían en nuestro programa de desarrollo de inteligencia emocional y los dividimos en 2 grupos:</p>
			
			<p class="lead text-justify"><b>Grupo uno:</b> El grupo de desarrollo – estas personas participarían en un programa de desarrollo de inteligencia emocional.</p>
			<p class="lead text-justify"><b>Grupo dos:</b> El grupo de control – sólo se evaluó la inteligencia emocional de estas personas y su desempeño de ventas.</p>

			<h3>¿En qué consistió el programa?</h3>
			<ul class="lead">
				<li>Una evaluación 360º de inteligencia emocional para gerente de ventas. Este informe nos permitió medir, desde su origen, los niveles de desempeño asociados con los ingresos de ventas de sus representantes de ventas.</li>
				<li>Talleres – Genos dictó un taller de un día completo y dos talleres de medio día con los representantes de ventas y sus gerentes para educarlos acerca de los beneficios de usar la inteligencia emocional en el entorno de ventas.</li>
				<li>Genos condujo ocho clases de evaluación en grupos pequeños con los gerentes de ventas.</li>
				<li>Genos también impartió cinco clases adicionales de coaching con inteligencia emocional, que incluyeron tanto a gerentes de ventas como a representantes de ventas. En estas clases de coaching enseñamos a los gerentes de ventas cómo mejorar la respuesta emocionalmente inteligente de sus representantes de ventas en sus entrevistas con clientes.</li>
				<li>Una evaluación 360º de inteligencia emocional para cada gerente de ventas, en donde volvimos a revisar el desempeño de los representantes de ventas e hicimos mediciones del retorno de la inversión (ROI) del programa.</li>
			</ul>

			<h5>Conclusiones</h5>
			<p class="lead text-justify">A lo largo del programa se observaron múltiples mejoras: la inteligencia emocional general de los gerentes de ventas mejoró en un promedio de 18%, un logró de por sí increíble. Observando el gráfico mostrado en esta sección, verán también que esta mejora tuvo un efecto evidente en el desarrollo del desempeño de ventas de los grupos de desarrollo, con una mejora promedio de 13% en comparación con el grupo de control. Después de completar el programa inicial, continuamos supervisando a todas las secciones y percibimos los siguientes resultados:</p>

			<div class="row text-center">
				<div class="col-sm-4" ><h5>Mes 1: Mejora de 7.1%</h5></div>
				<div class="col-sm-4" ><h5>Mes 2: Mejora de 15.4%</h5></div>
				<div class="col-sm-4" ><h5>Mes 3: Mejora de 13.4%</h5></div>
			</div>

			<p class="lead text-justify">Todos los porcentajes anteriores se calcularon por el crecimiento territorial en la venta minorista durante este período. No hubo cambios, ni se observaron mejoras dentro del grupo de control durante este intervalo de tiempo.</p>
