			<p class="lead text-justify">
				Para profundizar aún más en el retorno de la inversión (ROI) generado a través del programa Genos, pudimos determinar que por cada $1 invertido, Sanofi-Aventis obtuvo un retorno de $6. Este estudio realizado por Genos para Sanofi-Aventis es el único de su clase que muestra claramente incrementos directos en ventas como resultado de la mejora de la inteligencia emocional.
			</p>

			<h5>Ganador del Premio del Instituto Australiano de Aprendizaje y Desarrollo</h5>
			<p class="lead text-justify">Las mejoras en los ingresos generados por el Grupo de Desarrollo generaron aproximadamente $6 por cada $1 que Sanofi-Aventis invirtió en el programa piloto. Este es el único estudio de su tipo que exhibe mejoras directas en la última línea de la hoja de resultados (EBITDA), como consecuencia de las mejoras en inteligencia emocional. Este exitoso programa fue reconocido como un desarrollo excepcional y recibió el Premio de Honor al Mérito 2006 del Instituto Australiano de Aprendizaje y Desarrollo por su sobresaliente contribución a la práctica del aprendizaje y desarrollo.</p>

			<h5>Feedback de los participantes</h5>

			<p class="lead text-justify">Como en todos y cada uno de nuestros proyectos, nos gusta escuchar el feedback de todos los participantes involucrados en el estudio y agradecemos sus apreciaciones acerca de nuestros hallazgos.<br>Los participantes han manifestado con claridad que los beneficios del programa no sólo optimizaron su desempeño, sino que también ayudaron a mejorar de las relaciones entre los miembros del equipo y los gerentes. Antes del programa, los representantes y gerentes de ventas habían estado experimentando dificultades; esto se debía fundamentalmente a los cambios resultantes de las grandes fusiones en las que estaba participando la compañía. El análisis posterior mostró que se superaron los obstáculos de las fusiones y la unificación se convirtió en algo habitual.
			</p>

