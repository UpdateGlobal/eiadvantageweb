			<h5>Resumen</h5>
			<p class="lead text-justify">
				Genos International viene trabajando con IBM bajo un criterio de largo plazo y está constantemente comprometido en desarrollar programas de vanguardia que se enfocan en desarrollar habilidades a través de toda la estructura gerencial. Hasta la fecha, todo nuestro trabajo con IBM se ha concentrado en utilizar nuestras evaluaciones y programas de inteligencia emocional para afinar competencias de mentoring y enseñanza. Este estudio de caso se enfoca en un proyecto de investigación que Genos desarrolló con IBM para determinar la correlación entre los niveles de inteligencia emocional de los líderes de IBM y el compromiso que resultó de sus reportes directos.<br>

				Como puede verse en el siguiente gráfico y como quedó demostrado en años de investigación, los centros de trabajo de alto desempeño pueden ser consecuencia del compromiso a gran escala de los trabajadores y esto es más común en aquellos lugares que permiten que la gente disfrute un nivel más alto de emociones positivas.
				</p>
