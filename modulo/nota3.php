				<p class="lead text-justify">Genos sugirió que las indicaciones observacionales que muestran la correlación entre la inteligencia emocional de un líder y el compromiso de los trabajadores ayudaría a reforzar la noción de las organizaciones de que éste era, en efecto, el aspecto gerencial más importante (percepción, entendimiento y el control de las emociones) y ayudaría a promover personal de alto desempeño, en donde la gente se compromete con su trabajo y experimenta experiencias emocionales positivas en su centro de trabajo.</p>
				
				<h5>Nuestra Solución</h5>
				<p class="lead text-justify">Genos evaluó la inteligencia emocional de 200 líderes dentro del marco de IBM, usando el modelo 360º de Inteligencia Emocional. Luego calculamos la puntuación promedio de inteligencia emocional a través de las calificaciones emitidas por los demás. Genos también evaluó a los 438 reportes directos que trabajaban con estos 200 líderes. Esta evaluación se realizó usando la Encuesta Genos de Compromiso de los Trabajadores, que usamos para calcular el compromiso psicológico y emocional de cada persona con IBM. Calculamos analíticamente los resultados invitando a los encuestados que indiquen en qué medida demostraban las cuatro conductas indicadas en el siguiente gráfico:
